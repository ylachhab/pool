#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;
	int	c;

	i = 1;
	c = 0;
	while (nb > 0)
	{
		nb = nb - i;
		i = i + 2;
		c++;
	}
	if (nb == 0)
		return (c);
	else
		return (0);
}

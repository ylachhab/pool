int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	rest;

	i = 0;
	rest = 0;
	sign = 1;
	while ((str[i] == 32) || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			sign *= -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		rest = rest * 10 + str[i] - 48;
		i++;
	}
	return (rest * sign);
}
